// --- Booleans --- //

 //Quickly there are things called Reserved Words in JS
 // Words like 'true' and 'false' are reserved and cannot be used to create a variable.

 //This is because they are used for the data type 'boolean'

 //Just an FYI

 let isHuman = true;

 //Output is "true"
 isHuman

 isHuman = false;

 //Output is "false"
 isHuman

 //Remember how we can change data types
 isHuman = 1;

 //Output is "1"
 isHuman